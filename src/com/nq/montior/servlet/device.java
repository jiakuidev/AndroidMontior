package com.nq.montior.servlet;

import java.io.IOException;
import java.io.PrintWriter;
import java.util.ArrayList;

import javax.servlet.ServletContext;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import com.nq.montior.bean.DeviceInfo;

public class device extends HttpServlet {

	String serialNumber;

	/**
	 * Constructor of the object.
	 */
	public device() {
		super();
	}

	/**
	 * Destruction of the servlet. <br>
	 */
	public void destroy() {
		super.destroy(); // Just puts "destroy" string in log
		// Put your code here
	}

	/**
	 * The doGet method of the servlet. <br>
	 * 
	 * This method is called when a form has its tag value method equals to get.
	 * 
	 * @param request
	 *            the request send by the client to the server
	 * @param response
	 *            the response send by the server to the client
	 * @throws ServletException
	 *             if an error occurred
	 * @throws IOException
	 *             if an error occurred
	 */
	public void doGet(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		ServletContext application = this.getServletContext();
		serialNumber = request.getParameter("serialNumber");
		request.setAttribute("serialNumber", serialNumber);
		DeviceInfo deviceInfo;
		ArrayList<DeviceInfo> deviceInfos = (ArrayList<DeviceInfo>) application
				.getAttribute("deviceInfoList");
		for (int i = 0; i < deviceInfos.size(); i++) {
			deviceInfo = deviceInfos.get(i);
			if (deviceInfo.getSerialNumber().equals(serialNumber)) {
				request.setAttribute("deviceInfo", deviceInfo);
				break;
			}
		}
		request.getRequestDispatcher("device.jsp").forward(request, response);

	}

	/**
	 * The doPost method of the servlet. <br>
	 * 
	 * This method is called when a form has its tag value method equals to
	 * post.
	 * 
	 * @param request
	 *            the request send by the client to the server
	 * @param response
	 *            the response send by the server to the client
	 * @throws ServletException
	 *             if an error occurred
	 * @throws IOException
	 *             if an error occurred
	 */
	public void doPost(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {

		doGet(request, response);
	}

	/**
	 * Initialization of the servlet. <br>
	 * 
	 * @throws ServletException
	 *             if an error occurs
	 */
	public void init() throws ServletException {
		// Put your code here
	}

}
